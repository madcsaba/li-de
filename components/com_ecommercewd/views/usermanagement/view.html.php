class EcommercewdViewUsermanagement extends EcommercewdView {
    ////////////////////////////////////////////////////////////////////////////////////////
    // Events                                                                             //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constants                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Variables                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constructor & Destructor                                                           //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Public Methods                                                                     //
    ////////////////////////////////////////////////////////////////////////////////////////
    public function display($tpl = null) {
        $model = $this->getModel();

        $options_model = WDFHelper::get_model('options');
        $this->options = $options_model->get_options();

        $task = WDFInput::get_task();
        switch ($task) {
            case 'displaylogin':
                $this->_layout = 'displaylogin';
                break;
            case 'displayregister':
                $this->_layout = 'displayregister';
                $this->form_fields = $model->get_user_data_form_fields(true);
                $this->registration_data = $model->get_registration_data();
                break;
            case 'displayupdateuserdata':
                $this->_layout = 'displayupdateuserdata';
                $this->form_fields = $model->get_user_data_form_fields();
                $this->user_data = $model->get_user_data();
                break;
		}

        parent::display($tpl);
    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Getters & Setters                                                                  //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Private Methods                                                                    //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Listeners                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
}