    public function displayuseraccount() {
          
        if (WDFHelper::is_user_logged_in() == false) {
           
            if(WDFInput::get('type')){
                
                WDFHelper::redirect('usermanagement', 'displaylogin', '&tmpl=component&task=displaylogin&controller=usermanagement', '', WDFText::get('MSG_LOG_IN_FIRST'), 'message');
            }
            else
                WDFHelper::redirect('usermanagement', 'displaylogin', '', '', WDFText::get('MSG_LOG_IN_FIRST'), 'message');
        }
        parent::display();
    }


