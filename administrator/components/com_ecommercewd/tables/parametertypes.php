class EcommercewdTableParametertypes extends JTable {
    ////////////////////////////////////////////////////////////////////////////////////////
	// Events                                                                             //
	////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////
	// Constants                                                                          //
	////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////
	// Variables                                                                          //
	////////////////////////////////////////////////////////////////////////////////////////
    public $id = 0;
    public $name = '';

	////////////////////////////////////////////////////////////////////////////////////////
	// Constructor & Destructor                                                           //
	////////////////////////////////////////////////////////////////////////////////////////
    public function __construct(&$db) {
        parent::__construct('#__ecommercewd_parametertypes', 'id', $db);
    }

    ////////////////////////////////////////////////////////////////////////////////////////
	// Public Methods                                                                     //
	////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////
	// Getters & Setters                                                                  //
	////////////////////////////////////////////////////////////////////////////////////////
	public function __get($property) {
        if (property_exists($this, $property) == false) {
            return;
        }

        switch ($property) {
            default:
                return $this->$property;
                break;
        }
    }

    public function __set($property, $value) {
        if (property_exists($this, $property) == false) {
            return $this;
        }

        switch ($property) {
            default:
                $this->$property = $value;
                break;
        }

        return $this;
    }
    
    
	////////////////////////////////////////////////////////////////////////////////////////
	// Private Methods                                                                    //
	////////////////////////////////////////////////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////
	// Listeners                                                                          //
	////////////////////////////////////////////////////////////////////////////////////////
}