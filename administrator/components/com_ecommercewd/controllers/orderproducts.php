    public function remove() {
        WDFDb::remove_rows('', WDFInput::get('id'));

        WDFHelper::redirect('', 'explore', '', 'order_id=' . WDFInput::get('order_id') . '&callback=' . WDFInput::get('callback'), WDFText::get('MSG_ITEM_REMOVED'));
    }

    public function update() {
        WDFDb::store_input_in_row();

        WDFHelper::redirect('', 'explore', '', 'order_id=' . WDFInput::get('order_id') . '&callback=' . WDFInput::get('callback'), WDFText::get('MSG_ITEM_SAVED'));
    }


