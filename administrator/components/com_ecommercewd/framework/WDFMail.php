    /**
     * Gets component translated text.
     *
     * @param    string $mail_from sender mail.
     * @param    string $mail_to recipient mail.
     * @param    string $subject mail subject.
     * @param    string $mail_body mail body.
     *
     * @return  boolean  is mail sent.
     */
    public static function send_mail($mail_from, $mail_to, $subject, $mail_body, $isHTML = false , $attachment = false) {
        $mailer = JFactory::getMailer();

        $mailer->setSender($mail_from);
        $mailer->addRecipient($mail_to);
        $mailer->setSubject($subject);
        $mailer->setBody($mail_body);
        $mailer->isHTML($isHTML);
		
		if($attachment != false){
			$mailer->addAttachment($attachment);
		}		
        $is_mail_sent = $mailer->Send() === true ? true : false;

        return $is_mail_sent;
    }
	

