	public static function get_module_name( $id )
	{
		$db = JFactory::getDbo();
		$query = $db->getQuery(true);	
		
		$query->clear();
		$query->select('module');
		$query->from('#__modules');
	
		$query->where( "id = " . $id );

		$db->setQuery($query);
		$module_name = $db->loadResult();

		if ($db->getErrorNum()) {
			echo $db->getErrorMsg();
			return false;
		}
		
		return $module_name ? $module_name : false;

	}
	
	public static function get_module_params( $name )
	{		
		$db = JFactory::getDbo();
		$query = $db->getQuery(true);	
		
		$query->clear();
		$query->select('params');
		$query->from('#__modules');
	
		$query->where( "module = '" . $name . "'");

		$db->setQuery($query);
		$module_params = $db->loadResult();
	
		if ($db->getErrorNum()) {
			echo $db->getErrorMsg();
			return false;
		}
		return WDFJson::decode($module_params);
	}



