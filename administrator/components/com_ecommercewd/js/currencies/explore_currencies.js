function insertCurrencyData(name, code, sign) {
    var currencyData = [];
    currencyData["name"] = name;
    currencyData["code"] = code;
    currencyData["sign"] = sign;

    window.parent[_callback](currencyData);
    window.parent.SqueezeBox.close();
}


