function exploreCurrencies(payment_name) {
    var url = "index.php?option=com_ecommercewd&controller=currencies&task=explore_currencies&callback=insertCurrencyData&payment=" + payment_name + "&tmpl=component";
    openPopUp(url);
}

function insertCurrencyData(currencyData) {
    adminFormSet("name", currencyData["name"]);
    adminFormSet("code", currencyData["code"]);
    adminFormSet("sign", currencyData["sign"]);
}


