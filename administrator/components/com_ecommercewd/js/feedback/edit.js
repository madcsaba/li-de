jQuery(document).ready(function () {
Joomla.submitbutton = function(task)
{
	if( task != 'cancel' )
		jQuery('#text').val(jQuery('#text').val().replace(/</g,'111'));	
		
	Joomla.submitform(task, document.getElementById('adminForm'));		
}
});


