function onAdminFormSubmit(pressbutton) {
	if(pressbutton == 'edit'){
		jQuery('[name="cid[]"]:checked').each(function(){
			var type = jQuery(this).parent().parent().attr('data-type');			
			jQuery('[name=type]').val(type);
		});
	}
    submitform(pressbutton);
}
