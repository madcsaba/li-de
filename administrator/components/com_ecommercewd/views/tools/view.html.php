class EcommercewdViewTools extends EcommercewdView {
    ////////////////////////////////////////////////////////////////////////////////////////
    // Events                                                                             //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constants                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Variables                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Constructor & Destructor                                                           //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Public Methods                                                                     //
    ////////////////////////////////////////////////////////////////////////////////////////
    public function display($tpl = null) 
	{
        $this->create_toolbar();
		$model = $this->getModel();
		
		$this->rows = $model->get_rows();
		$this->sort_data = $model->get_rows_sort_data();
		$this->pagination = $model->get_rows_pagination();

        parent::display($tpl);
    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Getters & Setters                                                                  //
    ////////////////////////////////////////////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////
    // Private Methods                                                                    //
    ////////////////////////////////////////////////////////////////////////////////////////
    private function create_toolbar() 
	{				
		JToolBarHelper::title(WDFText::get('TOOLS_MANAGER_MANAGE'), 'spidershop_tools.png');
		JToolBarHelper::publishList('publish',WDFText::get('ENABLE'));
		JToolBarHelper::unpublishList('unpublish',WDFText::get('DISABLE'));
		JToolBarHelper::divider();
		JToolBarHelper::deleteList( WDFText::get('MSG_DELETE_CONFIRM'), 'remove',WDFText::get('UNINSTALL'));				

    }


    ////////////////////////////////////////////////////////////////////////////////////////
    // Listeners                                                                          //
    ////////////////////////////////////////////////////////////////////////////////////////
}